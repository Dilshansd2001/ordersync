export const getPlanKey = (business) =>
  business?.planCapabilities?.key || business?.subscriptionPlan || business?.current_plan || null

export const getPlanFeatureMap = (business) =>
  business?.planCapabilities?.features || PLAN_DEFINITIONS[String(getPlanKey(business) || '').toUpperCase()]?.features || {}

export const hasPlanFeature = (business, featureKey) => Boolean(getPlanFeatureMap(business)?.[featureKey])
